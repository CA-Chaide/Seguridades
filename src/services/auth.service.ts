
import type { Auth, Clase, UsuarioTipoUsuario } from "@/types/interfaces";
import type { BodyListResponse } from "@/types/body-list-response";
import type { BodyResponse } from "@/types/body-response";
import { environment } from "@/environments/environments.prod";

const API_URL = `${environment.apiURL}/api/auths/login`;

export const usuarioTipoUsuarioService = {

  async save(data: UsuarioTipoUsuario): Promise<BodyResponse<Auth>> {
    const response = await fetch(API_URL, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(data),
    });
    if (!response.ok) {
      const errorBody = await response.json().catch(() => ({ message: 'Error desconocido' }));
      throw new Error(errorBody.message || 'Failed to save clase');
    }
    return response.json();
  },

};
